var mark = prompt("enter your mark: ");




if (mark >= 80)
 document.write("You have got A+");

else if (mark >= 70)
 document.write("You have got A");

else if (mark >= 60)
 document.write("You have got A-");

else if (mark >= 50)
 document.write("You have got B");

else if (mark >= 40)
 document.write("You have got C");

else if (mark >= 33)
 document.write("You have got D");

else
 document.write("You have failed");
